     <h2>União Estável</h2>
     <br>
     <h3>O que é ?</h3>
     <font size='4'>
     <p>É a união entre um homem e uma mulher, configurada na convivência pública, contínua e duradoura com o objetivo de constituição de família. O Supremo Tribunal Federal atribuiu às uniões homoafetivas os mesmos efeitos da união estável heteroafetiva.

     Aplicam-se à união estável os deveres de lealdade, respeito, assistência, e de guarda, sustento e educação dos filhos.
    
     O casal pode formalizar a existência da união mediante escritura pública declaratória de união estável. A escritura pode ser utilizada para fixar a data do início da união estável, o regime de bens entre os conviventes, eventual alteração do nome, bem como para garantir direitos junto ao INSS, convênios médicos, odontológicos, clubes etc.</p>

     <h3>Quais são os requisitos da escritura de união estável?</h3>

      <p>A lei não exige prazo mínimo de duração da convivência para que se constitua a união estável e também não exige que o casal viva na mesma casa ou tenha o mesmo domicílio, bastando o intuito de constituir família.
     O casal interessado em formalizar a união estável por escritura pública deve comparecer ao Cartório de Notas portando os documentos pessoais originais e declarar a data de início da união, bem como o regime de bens aplicável à relação.
      Não há necessidade de presença de testemunhas na escritura. A união estável não se constituirá se houver impedimentos matrimoniais. Podem viver em união estável as pessoas casadas, desde que separadas de fato ou judicialmente</p>

      <h6><b>A certidão de União Estável não altera o Estado Cívil do Casal!</b></h6>
