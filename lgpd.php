     O <b> OFICIAL DE REGISTRO CIVIL E TABELIÃO DE NOTAS DE RIO DAS PEDRAS</b>, implementou em sua rotina a política de proteção de dados dos usuários de seus serviços, colaboradores e prestadores de serviços, em cumprimento ao <b> <u> Provimento nº 45/2021 do Tribunal de Justiça do Estado de São Paulo </u> </b> e à <b><u> Lei nº 13.709/2018 (LGPD)</u></b>, que se aplica às serventias extrajudiciais, nos seguintes termos:
