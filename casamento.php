        <div id="collapsefive" class="collapse" data-parent="#accordion">
        <div class="alert alert-dark" role="alert">  
          <div class="card-body">
            O Regime de bens consiste nas regras, estabelecidas pelos nubentes, que regerão o casamento quanto à questão patrimonial, durante o casamento e, se necessário, até a sua dissolução.
            O regime de bens deve ser escolhido quando os noivos fazem o pedido da habilitação do casamento em um Tabelionato de Notas. Os regimes de bens são:  <br> <br>

            <b>Comunhão Parcial de Bens</b>
            Todos os bens adquiridos após a data do casamento serão comuns ao casal, porém os bens adquiridos individualmente antes da união permanecem sendo propriedade individual de cada um. <br> <br>

            <b>Comunhão Total de Bens</b> 
            Todos os bens atuais e futuros de ambos serão comuns ao casal. <br> <br>

            <b>Separação Total de Bens</b> 
            Todos os bens atuais e futuros dos noivos continuarão sendo propriedade individual.  <br> <br>

            <b>Participação final nos Aquestos</b>
            Os bens que os cônjuges possuíam antes do casamento e os que foram adquiridos depois permanecem próprios de cada um, porém, se houver a dissolução do casamento (divórcio ou óbito), os bens que foram adquiridos na constância do casamento serão partilhados.
