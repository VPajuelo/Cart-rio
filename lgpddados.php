     <p> <font face = 'arial'>
     O <b> Com o objetivo de proteger os direitos fundamentais de liberdade e de privacidade e o livre desenvolvimento da personalidade da pessoa natural, elaboramos a presente Política de Privacidade, observadas as disposições da Lei Geral de Proteção de Dados Pessoais (LGPD) – Lei n° 13.709/2018.

A sua privacidade é importante para nós. É política do 2º Ofício de Notas e Protestos de Brasília respeitar a sua privacidade em relação a qualquer informação sua que possamos coletar no site 2º Ofício de Notas e Protestos de Brasília.

Para nós é importante ser transparente sobre o tratamento dos dados pessoais dos Usuários que utilizam nosso site, nos termos do Artigo 9º da LGPD. Esta Política se aplica quando o Usuário utiliza os nossos Serviços.

A presente Política de Privacidade tem por objetivo informar as hipóteses em que – no exercício da competência legal desta Serventia – são tratados dados pessoais, a sua finalidade, os procedimentos e as práticas utilizadas para a execução dessas atividades.

Solicitamos informações pessoais apenas quando realmente precisamos delas para lhe fornecer um serviço. Fazemo-lo por meios justos e legais, com o seu conhecimento e consentimento. Também informamos por que estamos coletando e como será usado.

Apenas retemos as informações coletadas para fornecer o serviço solicitado. Quando armazenamos dados, protegemos dentro de meios comercialmente aceitáveis ​​para evitar perdas e roubos, bem como acesso, divulgação, cópia, uso ou modificação não autorizados.

Não compartilhamos informações de identificação pessoal publicamente ou com terceiros, exceto quando exigido por lei.

O uso continuado de nosso site será considerado como aceitação de nossas práticas em torno de privacidade e informações pessoais. Se você tiver alguma dúvida sobre como lidamos com dados do usuário e informações pessoais, entre em contato conosco.

PARA QUÊ COLETAMOS OS SEUS DADOS?
Para promover segurança jurídica.

COMO COLETAMOS OS DADOS? QUAIS DADOS COLETAMOS?
Sempre que um usuário é recepcionado no 2º Ofício de Notas e Protestos de Brasília, coletamos os dados necessários para o cumprimento da legislação, sendo estes apenas os indispensáveis para a plena identificação e contato.

ONDE SEUS DADOS SÃO UTILIZADOS?
Na lavratura de atos notariais e na prestação de informações aos órgãos públicos e à Central Notarial de Serviços Eletrônicos Compartilhados – CENSEC.

TRANSPARÊNCIA
Antes de utilizar seus dados para fins diversos dos impostos pela legislação, você será consultado.

DIREITOS
Você sempre será informado sobre seus direitos e como exercê-los. Suas informações somente serão repetidas nos termos da legislação vigente.

SEGURANÇA E PRIVACIDADE
Nós gerenciamos e armazenamos os dados com o máximo de segurança e privacidade.

Mais informações
Esperemos que esteja esclarecido e, como mencionado anteriormente, se houver algo que você não tem certeza se precisa ou não, geralmente é mais seguro deixar os cookies ativados, caso interaja com um dos recursos que você usa em nosso site.

Esta Política de Privacidade será atualizada sempre que necessário, motivo pelo qual recomenda-se sua constante e periódica consulta.

