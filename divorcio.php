  <h3>O que é Divórcio Consensual?  </h3>

  <p>A separação pode ser convertida em divórcio, podendo também proceder a divórcio direto por escritura pública.</h3>
   
  <h3>O que é necessário: </h3>    

  <p>
    Todas as partes interessadas tem que estar assistidas e orientadas por advogado comum ou advogado de cada uma delas, cuja qualificação e assinatura constarão do ato notarial. </p>

  <h3>Documentos que deverão ser apresentados: </h3>

  <p>a) certidão de casamento (atualizada, 90 dias, CGJ/SP) (no caso de conversão, faz-se necessária a averbação da separação legal); <br>
      b) documento de identidade (ex.RG) e CPF;<br>
      c) pacto antenupcial, se houver; <br>
      d) certidão de nascimento ou outro documento de identidade oficial dos filhos absolutamente capazes, se houver; <br>
      e) certidão de propriedade de bens imóveis e direitos a eles relativos (atualizada, 30 dias; comprovante do valor venal, atualizado -CGJ/SP);<br>
      f) documentos necessários à comprovação dos bens móveis e direitos (comprovante do valor de mercado, se houver);<br>
       <br> 
     <b> Observações:</b>
      Os documentos apresentados no ato da escritura devem ser originais ou em cópias autenticadas, salvo os de identidade das partes, vedada a apresentação destes documentos replastificados, que sempre deverão ser os originais. (**) Desde que as partes não tenham filhos em comum menores e incapazes. 
    
