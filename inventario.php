    <h1 class="h3">Inventário e Sobrepartilha</h1>
    <div style="text-align: justify;">
    <font size='4'>
      <br>

     <p> Inventário é o procedimento utilizado para apuração dos bens, direitos e dívidas do falecido. Com a partilha é instrumentalizada a transferência da propriedade dos bens aos herdeiros.
        A Lei 11.441/07 facilitou a vida do cidadão e desburocratizou o procedimento de inventário ao permitir a realização desse ato em cartório, por meio de escritura pública, de forma rápida, simples e segura. <br> <br>
    
        <b>Atenção: mesmo que a pessoa tenha falecido antes da Lei 11.441/07, também é possível fazer o inventário por escritura pública, se preenchidos os requisitos da lei.</b></p>
      <br>
        <h3>Quais são os requisitos para a realização de um inventário em cartório?</h3>
        <p>Para que o inventário possa ser feito em cartório, é necessário observar os seguintes requisitos:
        (a) todos os herdeiros devem ser maiores e capazes;
        (b) deve haver consenso entre os herdeiros quanto à partilha dos bens;
        (c) o falecido não pode ter deixado testamento, exceto se o testamento estiver caduco ou revogado;
        Pelo Provimento 37/2016 da Corregedoria Geral da Justiça do Estado de São Paulo, ainda que haja testamento válido, se houver prévia autorização judicial, é possível que o inventário seja feito em um cartório de notas. (d) a escritura deve contar com a participação de um advogado.
        Se houver filhos menores ou incapazes o inventário deverá ser feito judicialmente. Havendo filhos emancipados, o inventário pode ser feito em cartório.
        A escritura de inventário não depende de homologação judicial.
        Para transferência dos bens para o nome dos herdeiros é necessário apresentar a escritura de inventário para registro no Cartório de Registro de Imóveis (bens imóveis), no Detran (veículos), no Cartório de Registro Civil de Pessoas Jurídicas ou na Junta Comercial (sociedades), nos bancos (contas bancárias) etc.  <br>

        <b>Atenção: caso exista inventário judicial em andamento, os herdeiros podem, a qualquer tempo, desistir do processo e optar pela escritura de inventário extrajudicial.</b>
        </p>
     
     <h3>Qual é o cartório competente para realização de um inventário?</h3>
      <p>O inventário extrajudicial pode ser feito em qualquer cartório de notas, independentemente do domicílio das partes, do local de situação dos bens ou do local do óbito do falecido. Não se aplicam as regras de competência do Código de Processo Civil ao inventário extrajudicial. <br> <br>

       <b> Atenção: as partes podem escolher livremente o tabelião de notas de sua confiança.</b></p>
     
     <h3>Quais são os documentos necessários para fazer um inventário em cartório?</h3> <br>
     
 

        <h4>Documentos do falecido</h4>

        <p>- RG, CPF, certidão de óbito, certidão de casamento (atualizada até 90 dias) e escritura de pacto antenupcial (se houver) <br>
            - Certidão comprobatória de inexistência de testamento expedida pelo Colégio Notarial do Brasil, através da Censec <a href="http://https://censec.org.br/"></a> <br>
            - Certidão Negativa da Receita Federal e Procuradoria Geral da Fazenda Nacional; <br>
            - Documentos do cônjuge, herdeiros e respectivos cônjuges; <br>
            - RG e CPF, informação sobre profissão, endereço, certidão de nascimento, certidão de casamento dos cônjuges (atualizada até 90 dias). <br></p>
        

            <h4>Documentos do advogado</h4>

            <p>- Carteira da OAB, informação sobre estado civil e endereço do advogado. <br>
                - Informações sobre bens, dívidas e obrigações, descrição da partilha e pagamento do ITCMD. <br>
                - imóveis urbanos: certidão de ônus expedida pelo Cartório de Registro de Imóveis (atualizada até 30 dias), carnê de IPTU, certidão negativa de tributos municipais incidentes sobre imóveis, declaração de quitação de débitos condominiais. <br></p>

                - imóveis rurais: certidão de ônus expedida pelo Cartório de Registro de Imóveis (atualizada até 30 dias), cópia autenticada da declaração de ITR dos últimos 5 (cinco) anos ou Certidão Negativa de Débitos de Imóvel Rural emitida pela Secretaria da Receita Federal – Ministério da Fazenda, Certificado de Cadastro de Imóvel Rural (CCIR) expedido pelo INCRA
                  <br> <br>
                - bens móveis: documento de veículos, extratos bancários, certidão da junta comercial ou do cartório de registro civil de pessoas jurídicas, notas fiscais de bens e joias, etc.
                <br> <br>

                <b>Atenção: o pagamento do Imposto de Transmissão Causa Mortis (ITCMD) deve ser efetuado em até 180 dias da data do óbito, sem incidência de multa.</b></p>

                <h3>É necessário contratar advogado para fazer o inventário em cartório?</h3>

                <p>A lei exige a participação de um advogado como assistente jurídico das partes nas escrituras de inventário. O tabelião, assim como o juiz, é um profissional do direito que presta concurso público, e age com imparcialidade na orientação jurídica das partes.
                    O advogado comparece ao ato na defesa dos interesses de seus clientes.
                    Os herdeiros podem ter advogados distintos ou um só advogado para todos.
                   
                    O advogado deverá assinar a escritura juntamente com as partes envolvidas. Não é necessário apresentar petição ou procuração, uma vez que esta é outorgada pelos interessados na própria escritura de inventário.
                    Atenção: Se um dos herdeiros for advogado, ele pode atuar também na qualidade de assistente jurídico na escritura.</p>
     
                 <h3>É possível ser representado por procurador na escritura de inventário?</h3>
                 <p>- Caso o interessado não possa comparecer pessoalmente ao cartório para assinar a escritura de inventário, ele poderá nomear um procurador por meio de procuração pública, feita em cartório de notas, com poderes específicos para essa finalidade.</p>
     
                 <b>Atenção: o ato de abertura de firma não é cobrado, mas o cartório é autorizado a extrair, às expensas do interessado, cópia dos documentos de identidade apresentados para arquivamento junto a sua ficha de firma. <br></b>
                  <br>
                 <h3>O que é inventário negativo?</h3>

                <p>O inventário negativo é utilizado para comprovar a inexistência de bens a partilhar.
                Ele é necessário caso os herdeiros queiram comprovar que o falecido deixou apenas dívidas, ou caso o cônjuge sobrevivente queira escolher livremente o regime de bens de um novo casamento.</p>

                <h3>O que é sobrepartilha?</h3>

                <p>Se após o encerramento do inventário os herdeiros descobrirem que algum bem não foi inventariado, é possível realizar a sobrepartilha por meio de escritura pública, observados os seguintes requisitos: (a) herdeiros maiores e capazes; (b) consenso entre os herdeiros quanto à partilha dos bens; (c) inexistência de testamento (desde que não esteja caduco ou revogado), exceto se houver prévia decisão judicial autorizando o inventário em cartório; (d) participação de um advogado. <br>

                 A sobrepartilha pode ser feita extrajudicialmente, a qualquer tempo, ainda que a partilha anterior tenha sido feita judicialmente e ainda que os herdeiros, hoje maiores, fossem menores ou incapazes ao tempo da partilha anterior.</p>

                <h3>Pode ser reconhecida a união estável em inventário?</h3>

                <p>Se o falecido vivia em união estável, os herdeiros podem reconhecer a existência dessa união na escritura de inventário.
                Se o companheiro for o único herdeiro ou se houver conflito entre ele e os demais herdeiros, o reconhecimento da união estável deve ser feito judicialmente.
                 É reconhecida como entidade familiar a união estável entre o homem e a mulher, configurada na convivência pública, contínua e duradoura com o objetivo de constituição de família. O Supremo Tribunal Federal atribuiu às uniões homoafetivas os mesmos efeitos da união estável heteroafetiva.</p>

                <h3>É possível renunciar à herança?</h3>

                <p>Se o herdeiro não tiver interesse em receber a herança, a renúncia pode ser feita por escritura pública.</p>

                <h3>É possível fazer em cartório o inventário de bens situados no exterior?</h3>

                <p>Se o falecido deixar bens situados no exterior não é possível fazer o inventário por escritura pública.</p>
        
